const messages = {
  en_gb: enGB,
  en_us: enGB,
  ...generatedMessages,
}

const legacyTextToKey = Object.fromEntries(
  Object.entries(enGB).map(([key, value]) => [String(value).replace(/\s+/g, " ").trim(), key]),
)

export function htmlLangForLocale(locale) {
  return languageOptions.find((item) => item.code === locale)?.htmlLang || "en-GB"
}

export function isRTLLocale(locale) {
  return Boolean(languageOptions.find((item) => item.code === locale)?.rtl)
}

function formatMessage(message, params = {}, messageKey = "") {
  const compactKey = messageKey.startsWith("time.")
  const needsSpace = (char) => Boolean(char && /[\p{L}\p{N}]/u.test(char))
  const source = String(message || "")
  return source.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key, offset) => {
    if (!Object.prototype.hasOwnProperty.call(params, key)) return match
    const value = params[key]
    const rendered = value == null ? "" : String(value)
    if (compactKey || !rendered) return rendered
    const previous = source[offset - 1] || ""
    const next = source[offset + match.length] || ""
    const prefix = needsSpace(previous) ? " " : ""
    const suffix = needsSpace(next) ? " " : ""
    return `${prefix}${rendered}${suffix}`
  })
}

export function translateMessage(locale, key, params = {}) {
  const normalizedLocale = languageOptions.some((item) => item.code === locale) ? locale : "en_gb"
  const fallback = messages.en_gb[key] || key
  const direct = messages[normalizedLocale]?.[key]
  return formatMessage(direct || fallback, params, key)
}

export function translateText(locale, text) {
  const normalized = String(text || "").replace(/\s+/g, " ").trim()
  if (!normalized) return normalized
  const key = legacyTextToKey[normalized]
  return key ? translateMessage(locale, key) : normalized
}
